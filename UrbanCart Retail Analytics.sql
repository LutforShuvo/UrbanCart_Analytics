	-- 15. How does customer purchasing behavior change over time since account creation?
	
	SELECT
		CASE
			WHEN TENURE <= 30 THEN '0 - 1 Month'
			WHEN TENURE <= 90 THEN '1 - 3 Month'
			WHEN TENURE <= 180 THEN '4 - 6 Month'
			WHEN TENURE <= 365 THEN '6 - 12 Month'
			ELSE '12+ Month'
		END AS TENURE_BUCKET,
		ROUND(AVG(TOTAL_REVENUE)) AS AVG_REVENUE
	FROM
		(
			SELECT
				FO.ORDER_ID,
				FO.ORDER_DATE,
				DC.CREATED_AT,
				FO.ORDER_DATE - DC.CREATED_AT AS TENURE,
				SUM(DP.UNIT_PRICE * FOI.QUANTITY) AS TOTAL_REVENUE
			FROM
				"FactOrders" FO
				JOIN "DimCustomers" DC ON FO.CUSTOMER_ID = DC.CUSTOMER_ID
				JOIN "FactOrderItems" FOI ON FO.ORDER_ID = FOI.ORDER_ID
				JOIN "DimProducts" DP ON DP.PRODUCT_ID = FOI.PRODUCT_ID
			GROUP BY
				FO.ORDER_ID,
				FO.ORDER_DATE,
				DC.CREATED_AT
		) AS ORDER_TOTALS
	GROUP BY
		TENURE_BUCKET
	ORDER BY
		MIN(TENURE)

		
