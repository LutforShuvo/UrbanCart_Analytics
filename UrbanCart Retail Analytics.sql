	-- 9. What is the average order value (AOV) and Average Basket Size?
	WITH
		NUMBER_OF_ORDERS AS (
			SELECT
				COUNT(*) AS TOTAL_ORDERS
			FROM
				"FactOrders"
		),
		TOTAL_REVENUE AS (
			SELECT
				SUM(DP.UNIT_PRICE * FOI.QUANTITY) AS TOTAL_REVENUE
			FROM
				"FactOrders" FO
				LEFT JOIN "FactOrderItems" FOI ON FO.ORDER_ID = FOI.ORDER_ID
				LEFT JOIN "DimProducts" DP ON FOI.PRODUCT_ID = DP.PRODUCT_ID
		)
		
	SELECT
		ROUND(TOTAL_REVENUE::NUMERIC / TOTAL_ORDERS, 2) AS AVERAGE_ORDER_VALUE
	FROM
		NUMBER_OF_ORDERS,
		TOTAL_REVENUE;
		
	
	WITH
