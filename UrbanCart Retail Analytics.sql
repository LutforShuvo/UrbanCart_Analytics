	-- 25. Based on product co-occurrence and customer behavior, which products should UrbanCart promote together to maximize cross-selling opportunities?
	WITH
		ORDER_TOTAL AS (
			SELECT
				COUNT(DISTINCT ORDER_ID) TOTALS
			FROM
				"FactOrderItems"
		),
		PRODUCT_ORDERS AS (
			SELECT	
				PRODUCT_ID,
				COUNT(DISTINCT ORDER_ID) PRO_TOTAL
			FROM
				"FactOrderItems"
			GROUP BY
				PRODUCT_ID
		),
		
		PAIR_FREQUENCY AS (
			SELECT
				LEAST(FOI1.PRODUCT_ID, FOI2.PRODUCT_ID) PRO1_ID,
				P1.PRODUCT_NAME PRODUCT1,
				GREATEST(FOI1.PRODUCT_ID, FOI2.PRODUCT_ID) PRO2_ID,
				P2.PRODUCT_NAME PRODUCT2,
				COUNT(DISTINCT FOI1.ORDER_ID) PAIR_BOUGHT
			FROM
				"FactOrderItems" FOI1
				JOIN "FactOrderItems" FOI2 ON FOI1.ORDER_ID = FOI2.ORDER_ID
				AND FOI1.PRODUCT_ID < FOI2.PRODUCT_ID
				JOIN "DimProducts" P1 ON FOI1.PRODUCT_ID = P1.PRODUCT_ID
				JOIN "DimProducts" P2 ON FOI2.PRODUCT_ID = P2.PRODUCT_ID
			GROUP BY
				P1.PRODUCT_NAME,
				P2.PRODUCT_NAME,
				PRO1_ID,
				PRO2_ID
		),
		FINAL_SELECT AS (
			SELECT
				PF.PRO1_ID,
				PF.PRODUCT1,
				PF.PRO2_ID,
				PF.PRODUCT2,
				PF.PAIR_BOUGHT,
				ROUND(
					(
						(PF.PAIR_BOUGHT::FLOAT / OT.TOTALS) / (
							(PO1.PRO_TOTAL::FLOAT / OT.TOTALS) * (PO2.PRO_TOTAL::FLOAT / OT.TOTALS)
						)
					)::NUMERIC,
					2
				) AS LIFT
			FROM
				PAIR_FREQUENCY PF
				CROSS JOIN ORDER_TOTAL OT
				JOIN PRODUCT_ORDERS PO1 ON PO1.PRODUCT_ID = PF.PRO1_ID
				JOIN PRODUCT_ORDERS PO2 ON PO2.PRODUCT_ID = PF.PRO2_ID
			ORDER BY
				LIFT DESC
	)
	SELECT
		*
	FROM
		FINAL_SELECT
	WHERE
		LIFT > 1
