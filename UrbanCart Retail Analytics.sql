	-- 3. Which cities generate the highest number of orders?
	
	SELECT
		DC.CITY,
		COUNT(*) AS NO_OF_ORDERS
	FROM "FactOrders" AS FO
		JOIN "DimCustomers" AS DC ON FO.CUSTOMER_ID = DC.CUSTOMER_ID
	GROUP BY DC.CITY
	ORDER BY count(*) DESC
	LIMIT 1;

	
	-- 4. What percentage of customers have placed more than one order?
	WITH
		REPEAT_CUSTOMERS AS (
			SELECT COUNT(*) AS REPEAT_COUNT
			FROM(
					SELECT
						CUSTOMER_ID
					FROM
						"FactOrders"
					GROUP BY
						CUSTOMER_ID
					HAVING
						COUNT(*) > 1
				) AS SUBQUERY
		),
		TOTAL_CUSTOMERS AS (
			SELECT
				COUNT(DISTINCT (CUSTOMER_ID)) AS TOTAL_COUNTS
			FROM
				"FactOrders"
		) --calculating total customers 
	SELECT
		ROUND(
			(RC.REPEAT_COUNT::NUMERIC / TC.TOTAL_COUNTS) * 100,
			2
		) || ' %' AS REPEAT_RATE_PERCENTAGE
	FROM
		REPEAT_CUSTOMERS RC,
		TOTAL_CUSTOMERS TC;
		
	
